package com.whatlookingfor.core.exception;


/**
 * IllegalArgumentException异常的封装处理
 *
 * @author Jonathan
 * @version 2016/6/27 11:41
 * @since JDK 7.0+
 */
public class IllegalParameterException extends BaseException {

	public IllegalParameterException() {
	}

	public IllegalParameterException(Throwable ex) {
		super(ex);
	}

	public IllegalParameterException(String message) {
		super(message);
	}

	public IllegalParameterException(String message, Throwable ex) {
		super(message, ex);
	}

}
